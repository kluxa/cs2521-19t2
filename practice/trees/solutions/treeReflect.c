
#include <stdlib.h>

#include "tree.h"

/**
 * Transforms a tree into the mirror image of itself.
 *
 * Examples:
 *           input tree         |         resulting tree
 * -----------------------------+---------------------------------
 *               5              |               5
 *              / \             |              / \
 *             2   9            |             9   2
 * -----------------------------+---------------------------------
 *               4              |               4
 *              / \             |              / \
 *             3   7            |             7   3
 *            /   / \           |            / \   \
 *           1   6   9          |           9   6   1
 * -----------------------------+---------------------------------
 *               6              |               6
 *              / \             |              / \
 *             4   9            |             9   4
 *            /                 |                  \
 *           3                  |                   3
 *          /                   |                    \
 *         1                    |                     1
 *          \                   |                    /
 *           2                  |                   2
 * -----------------------------+---------------------------------
 *               7              |               7
 * -----------------------------+---------------------------------
 *               X              |               X
 */
void treeReflect(Tree t) {
	if (t == NULL) return;
	
	Tree temp = t->left;
	t->left = t->right;
	t->right = temp;
	
	treeReflect(t->left);
	treeReflect(t->right);
}

